gwUINT8 crc_calc(const gwUINT8 *data, gwUINT8 len)
{
	gwUINT8 crc;

	crc = 0;//crc_init();

	if (len == 5) {
		crc = crc_next4(crc, data[2], data[1], data[0], data[6]);
		crc = crc_next(crc, data[5]);
	} else {
		while (len >= 16) {
			len -= 16;
			crc = crc_next4(crc, data[0], data[1], data[2], data[3]);
			crc = crc_next4(crc, data[4], data[5], data[6], data[7]);
			crc = crc_next4(crc, data[8], data[9], data[10], data[11]);
			crc = crc_next4(crc, data[12], data[13], data[14], data[15]);
			data += 16;
		}

		while (len--) {
			crc = crc_next(crc, *data++);
		}
	}

//	return crc_final(crc);
	return crc + 1;
}
